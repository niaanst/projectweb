							<p>Museum Pertanian terletak satu kompleks dengan Museum Tanah dan berada tak jauh dari seberang pintu utama Kebun Raya Bogor. Berdirinya Museum Pertanian tidak dapat dipisahkan dari Museum Tanah, yang telah diresmikan Mentan, tepat pada peringatan Hari Tanah Sedunia,  5 Desember 2017.</p>
							<p>Museum Pertanian terletak satu kompleks dengan Museum Tanah dan berada tak jauh dari seberang pintu utama Kebun Raya Bogor. Berdirinya Museum Pertanian tidak dapat dipisahkan dari Museum Tanah, yang telah diresmikan Mentan, tepat pada peringatan Hari Tanah Sedunia,  5 Desember 2017.</p>
              <p>Saat masuk ke museum , pengunjung akan disuguhkan Galeri Pangan dan Peradaban Pertanian, yang menampilkan informasi tentang sejarah komoditas pangan beserta peradaban yang menyertainya.</p>
              <p>Berbagai informasi komoditas pangan di Indonesia diilustrasikan dalam bentuk diorama pengolahan lahan sawah, rumah petani hingga alat pertanian yang sangat menarik untuk dijadikan spot foto bagi para pengunjung. Museum Pertanian menawarkan sensasi yang menyenangkan dengan dekorasi yang sangat Instagramable.</p>
              <p>Beranjak ke lantai 2 dari museum ini, pengunjung akan mendapatkan Galeri Kebijakan dan Komoditas, yang menggambarkan perkembangan pertanian Indonesia dari era kolonial (VOC dan Pemerintah Belanda) pada 1600 - 1945 hingga perkembangan pertanian pada era setelah kemerdekaan, yaitu 1945 - 2019.</p>
              <p>Yang menarik, selain memiliki zona Instagramable, di lokasi yang merangkum pembangunan pertanian dari tahun 1600 sampai  2019 ini juga terdapat coffee corner.</p>
              <p>Tidak berhenti di situ, pengunjung juga akan dimanjakan di Galeri Pertanian Masa Depan dan Lumbung Pangan Dunia 2045 yang terdapat di lantai 3. Di sini, pengunjung dapat melihat pengembangan pertanian 4.0 dengan sangat apik.</p>
              <p>Ilustrasi penggunaan teknologi canggih seperti drone, market smart farming hingga atutonomous tractor, yaitu traktor tanpa awak yang menggunakan sistem GPS berbasis Real Time Kinematika, juga dapat dinikmati pengunjung.</p>
              <p>Galeri keempat dari Museum ini terletak di Gedung D, yang juga masih berada di kompleks Museum Tanah dan Pertanian Bogor. Galeri ini memiliki lima zona ruangan terpisah dengan titik edukasi dan visualisasi yang menarik.</p>
              <p>Galeri Peternakan menampilkan berbagai perkembangan teknologi peternakan yang disajikan dalam bentuk video pendek, diorama, poster dan beberapa artefak. Ke depan, lantai 4 Gedung D akan dibangun zona rooftop, yang memungkinkan pengunjung untuk menikmati panorama Gunung Salak, dengan fasilitas gazebo sederhana yang dikelilingi sarana edukasi hidroponik.</p>
